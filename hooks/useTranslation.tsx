export function useTranslation() {
  const { language } = useLayoutStore()

  const t = useCallback(
    (key: string) => {
      const translations = language === "ar" ? ar : en
      
      if (translations[key as keyof typeof translations]) {
        return translations[key as keyof typeof translations]
      }
      
      if (key.charAt(0) === key.charAt(0).toUpperCase()) {
        const lowercaseKey = key.charAt(0).toLowerCase() + key.slice(1)
        if (translations[lowercaseKey as keyof typeof translations]) {
          return translations[lowercaseKey as keyof typeof translations]
        }
      }
      
      if (key.charAt(0) === key.charAt(0).toLowerCase()) {
        const uppercaseKey = key.charAt(0).toUpperCase() + key.slice(1)
        if (translations[uppercaseKey as keyof typeof translations]) {
          return translations[uppercaseKey as keyof typeof translations]
        }
      }
      
      return key
    },
    [language],
  )

  return { t }
}
