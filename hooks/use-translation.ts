export function useTranslation() {
  const { language } = useLayoutStore()

  const t = useCallback(
    (key: string) => {
      const translations = language === "ar" ? ar : en
      return translations[key as keyof typeof translations] || key
    },
    [language],
  )

  return { t }
}
